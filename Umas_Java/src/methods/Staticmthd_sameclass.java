package methods;

public class Staticmthd_sameclass {
	//main method declaration
	public static void main(String[] args)
	{
		System.out.println("Main method started");//method definition
		demo();//static method name called in main method
		System.out.println("Main method ended");
	}
	//static regular method declaration from same class
	public static void demo() //demo() is method name
	{
		System.out.println("I am static regular method");//method definition
		
	//one complete method consists of method declaration and method definition	
	}
}
