package mypackage;

public class variables {

	public static void main(String[] args)
	{
		//single line comment
		//Variable declaration
		int rollno;
		
		//Variable assigning
		rollno=50;
		
		//Usage/print
		System.out.println(rollno);				//here rollno is a variable
		System.out.println("My RollNumber is"); //My RollNumber is a message
		System.out.println("My RollNumber is "+rollno);
		/*Multiple line comment
		 * to print message and variable in single line
		 * use + as a concatenate operator*/
		
		int a=10;
		char b='H';
		int v=a+b;
		System.out.println(v);

	}

}
