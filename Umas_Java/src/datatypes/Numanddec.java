package datatypes;

public class Numanddec {

	public static void main(String[] args)
	{
		float weight;
		weight=99.98656798f;
		System.out.println("My weight is "+weight+" kg");
	
		double price;
		price=85.4568793987d;
		System.out.println("price is "+price);
	
		float f1=35e3f;//scientific declaration
		double f2=15e2d;
		System.out.println(f1);
		System.out.println(f2);
	}

}
